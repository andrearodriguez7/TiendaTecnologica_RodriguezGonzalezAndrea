private void buscarProductoPorId(JTextField campoTexto) {
    Connection conexion = Conexion.getConnection();
    if (conexion == null) {
        System.out.println("No se pudo conectar a la base de datos.");
        return;
    }

    // Obtener el texto del campo de b√∫squeda y verificar si est√° vac√≠o
    String textoIngresado = campoTexto.getText().trim();
    if (textoIngresado.isEmpty()) {
        System.out.println("El campo de texto est√° vac√≠o. Por favor, ingresa un ID de producto.");
        return;
    }

    // Intentar convertir el texto ingresado en un n√∫mero entero
    int productoId;
    try {
        productoId = Integer.parseInt(textoIngresado);
    } catch (NumberFormatException ex) {
        System.out.println("Entrada inv√°lida. El ID debe ser un n√∫mero entero.");
        return;
    }

    // Consulta para verificar si existe un producto con el ID proporcionado
    String consultaSQL = "SELECT id_producto, nombre FROM productos WHERE id_producto = ?";

    try (PreparedStatement consultaPreparada = conexion.prepareStatement(consultaSQL)) {
        consultaPreparada.setInt(1, productoId); // Vincular el par√°metro
        ResultSet resultado = consultaPreparada.executeQuery();

        if (resultado.next()) {
            String nombreProducto = resultado.getString("nombre");
            System.out.println("Producto encontrado: " + nombreProducto + " (ID: " + productoId + ")");
        } else {
            System.out.println("No se encontr√≥ ning√∫n producto con el ID " + productoId + ".");
        }

    } catch (SQLException ex) {
        System.out.println("Error al buscar el producto en la base de datos: " + ex.getMessage());
        ex.printStackTrace();
    } finally {
        try {
            conexion.close();
        } catch (SQLException ex) {
            System.out.println("Error al cerrar la conexi√≥n: " + ex.getMessage());
        }
    }
}

    
